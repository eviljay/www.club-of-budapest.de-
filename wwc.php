					<h2>Mission</h2>
					<p>The World Wisdom Council (WWC) has been convened by the Club of Budapest in cooperation with the <a href="http://globalspirit.org" target="_blank">World Commission on Global Consciousness and Spirituality</a> in the conviction that the paramount requirement in this age of discontinuity and transformation is to recognize that, through the development of a new dimension of consciousness, the world can be constructively changed by women and men wherever they live and whatever their interests and lot in life.</p>
					<p>The task of the Council is to build on the power and creativity innate in all people by:</p>
					<ul>
						<li>bringing to the attention of the widest layers of the public both the dangers and the opportunities inherent in the human condition in its global dimension;</li>
						<li>identifying priority areas where individual and cooperative action is needed in order to reinforce progress toward peace and sustainability, locally as well as globally;</li>
						<li>offering guidance for developing the individual and collective wisdom that empowers action capable of bringing about constructive change in the local and the global economic, social, and ecological environment.</li>
					</ul>
					<p>The World Wisdom Council realizes that there is already a growing range of initiatives aligned with its mission. In consequence it is taking as one of its highest priorities the formation of networks, partnerships, and collaborations in the interest of mobilizing the forces required for constructive transformation on a global scale.</p>
					<p>The World Wisdom Council is politically, socially, and culturally non-partisan, championing the joint interest of all humans and all life on this planet, informing people so that they can move toward a world where they can live in peace with each other and in harmony with nature.</p>
