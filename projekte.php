		<h1>Projekte</h1>
		
		<p>Statt vieler schöner Worte wollen wir unmittelbar zu Aktion einladen. Auf unserer Webseite stellen wir Projekte vor, die wir selbst initieren, an denen wir uns beteiligen oder die wir unterstützenswert finden. Überall fehlt es an Geld. Nicht, weil kein Geld da wäre. Es ist nur im falschen System gefangen. Eines unserer sechs Arbeitsfelder ist die Ökonomie. Der größte Teil dessen, was wir untereinander austauschen, und tatsächlich alles, wovon wir leben, ist geschenkt! Wir wollen die Qualität des Schenkens in das Wirtschaftshandeln zwischen den Menschen zurückbringen und engagieren uns deshalb für eine Ökonomie des Schenkens. Jedes Projekt, das Sie auf diesen Seiten finden, verdient, dass Sie ihm etwas schenken. Das wird in den meisten Fällen Geld sein, das Sie hier online auf den Weg bringen können. Das kann aber auch ehrenamtliche Mitwirkung oder ein anderer materieller oder immaterieller Beitrag sein.</p>

		<p>Wir sorgen dafür, dass zwischen Ihnen und dem Projekt die nötige Transparenz gewahrt wird. Sie sollen wissen, was wofür gebraucht wird, wie Ihre Schenkung verwendet wird und wie sich das Projekt weiterentwickelt. Vieles erfahren Sie direkt auf den Webseiten der Projekte. Wir geben Ihnen noch die Perspektive des Beraters, des wohlwollenden Begleiters und notfalls auch des unbefangenen Kritikers dazu.</p>
		
