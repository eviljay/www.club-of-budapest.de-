					<p>Founded in 1993, the global Club of Budapest is an informal international association dedicated to developing a new way of thinking and a new ethics that will help resolve the social, political, economic, and ecological challenges of the 21st century. With its roster of internationally renowned members the Club initiates a dialogue between different belief systems and world views in order to co-create and develop effective strategies for responsible and sustainable action with a global focus.</p>
					<p>The idea of the Club of Budapest was developed in 1978 in a discussion between Aurelio Peccei, founder and first president of the Club of Rome, and Ervin Laszlo, systems philosopher and also member of the Club of Rome at that time. They were convinced that the enormous challenges to humanity can only be dealt with through the development of a cultural and cosmopolitan consciousness. Based on these ideas, the Club of Budapest was founded by Dr. Laszlo in 1993. The founding city and namesake of the Club lies at the heart of Europe and is spread out over both banks of the River Danube. The successful merging of the two cities Buda and Pest is symbolized by the famous Chain Bridge. It visualizes our ambition to build bridges between generations, disciplines and cultures. Therefore, it was selected as the logo and signet for the objectives of the Club. The main essence of the global efforts lies in the initiation of dialogue.</p>
					<p>Since 2008 the operational platform of the Club of Budapest is the WorldShift Network, which was established by Ervin Laszlo, Wolfgang Riehn and Johannes Heimrath in 2007 as an international foundation. It has its seat in Germany. In future the WorldShift Network will be responsible for the international projects of the Club as well as for the coordination of the National Clubs.</p>
