					<h1>The Club of Budapest Awards</h1>
					<p>During the years 1996 to 2004 the Club of Budapest issued a series of awards for outstanding examples of global consiousness in action. The prizes were given in two categories:</p>
					<h2>Planetary Consciousness Award</h2>
					<p>The Club of Budapest's Planetary Consciousness Prizes are awarded in recognition of the urgent necessity to upgrade the thinking and feeling - the consciousness - of a critical mass of people in today's societies. The problems faced by the world community today are due above all to a lag in the consciousness of people and leaders with respect to the rapidity with which new conditions evolve on this planet. The Prizes honour individuals who, in their sphere of personal and professional activity, have been most effective in exhibiting and promoting the universally required new consciousness.</p>
					<p><a href="laureats-planet-consc.php">» The Laureats of the Planetary Consciousness Award</a></p>
					<h2>Change the World—Best Practice Award</h2>
					<p>Change occurs through innovation. With the help of Best Practice Projects these innovations can be translated into deeds in an intelligent manner. If we desire change towards an ecologically and socially sustainable world, we should develop a special ability to recognize and promote the best practice projects in the world concerning sustainable development - in other words, projects that:</p>
					<ul>
						<li>put into practice the criteria of &quot;Agenda 21&quot; for socially and ecologically sustainable global development in a particularly innovative, exemplary, successful and integral way;</li>
						<li>have a special potential to initiate fundamental processes of change towards a globally responsible and win-win oriented kind of thinking;</li>
						<li>have an implicit potential for applicability under different circumstances.</li>
					</ul>
